used_phones = set()

def random_phone():
    while True:
        phone = '+3069' + str(random.randint(10000000, 99999999))
        if phone not in used_phones:
            used_phones.add(phone)
            return phone

GREEKLISH = {
    'α':'a',  'ά':'a',  'β':'v',  'γ':'g',  'δ':'d',
    'ε':'e',  'έ':'e',  'ζ':'z',  'η':'i',  'ή':'i',
    'θ':'th', 'ι':'i',  'ί':'i',  'ϊ':'i',  'ΐ':'i',
    'κ':'k',  'λ':'l',  'μ':'m',  'ν':'n',  'ξ':'x',
    'ο':'o',  'ό':'o',  'π':'p',  'ρ':'r',  'σ':'s',
    'ς':'s',  'τ':'t',  'υ':'y',  'ύ':'y',  'ϋ':'y',
    'ΰ':'y',  'φ':'f',  'χ':'ch', 'ψ':'ps', 'ω':'o',  'ώ':'o',
}

def to_greeklish(text):
    result = ''
    for ch in text.lower():
        result += GREEKLISH.get(ch, ch)
    return result

def random_email(onoma, eponymo, i):
    gl_onoma  = to_greeklish(onoma)
    gl_eponymo = to_greeklish(eponymo)
    return f'{gl_onoma}.{gl_eponymo}{i}@email.gr'

